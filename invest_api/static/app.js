  function paramsFor(endpoint, state) {
    const params = new URLSearchParams();
    Object.entries(state).forEach(([key, value]) => {
      if (key === "variant" || key === "metric" || value === "") return;
      if (endpoint === "tax_events" && !["start", "end", "adjust_cpi", "initial"].includes(key)) return;
      if (endpoint === "commission_effect" || endpoint === "commission_tax") {
        if (!["start", "end", "adjust_cpi", "commissions", "initial"].includes(key)) return;
      }
      if (endpoint === "sp500_risk" && !["start", "end", "years", "threshold"].includes(key)) return;
      if (endpoint === "kupat_gemel" || endpoint === "kupat_gemel_pension") {
        const allowed = [
          "age",
          "start_year",
          "initial",
          "pension_months",
          "adjust_cpi",
          "kh_buy_sell",
          "kh_annual_fee",
          "ph_buy_sell",
          "ph_annual_fee",
          "ii_buy_sell",
          "ii_annual_fee"
        ];
        if (!allowed.includes(key)) return;
        if (endpoint === "kupat_gemel" && key === "pension_months") return;
      }
      if (endpoint === "independent_commissions" || endpoint === "tax_us_vs_il") {
        if (!["start", "end", "initial", "share_price", "dollar_commission", "commission_per_share", "min_dollar_commission", "yearly_commission"].includes(key)) return;
      }
      if (endpoint === "portfolio_summary" || endpoint === "portfolio_over_time") {
        if (!["portfolios", "year_start", "year_end", "commission_adjusted", "value_mode", "rolling_window", "full_history_rolling"].includes(key)) return;
      }
      if (endpoint === "us_world_rolling" || endpoint === "us_global_rolling") return;
      if (endpoint === "global_vs_sp500_heatmap" && key !== "max_years") return;
      if (endpoint === "sp500_scv_heatmap" && key !== "max_years") return;
      if ((endpoint === "sp500_scv_rolling" || endpoint === "sp500_scv_after_tax") && key !== "window") return;
      if (endpoint === "trinity" && key === "yearly_draw") {
        params.set(key, String(Number(value) / 100));
        return;
      }
      if (key === "commissions" || key === "portfolios") {
        value.split(",").map((part) => part.trim()).filter(Boolean).forEach((part) => params.append(key, part));
      } else if (endpoint === "sp500_risk" && key === "threshold") {
        params.set(key, String(Number(value) / 100));
      } else {
        params.set(key, value);
      }
    });
    return params;
  }

