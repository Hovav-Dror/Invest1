      return `<text x="${pad.left + 22}" y="${pad.top + 28}" fill="#111" font-size="13" font-weight="700">${escapeHtml(`${chartConfig.leadAnnotation.label} in ${wins} / ${comparisons} cases (${pct}%)`)}</text>`;
