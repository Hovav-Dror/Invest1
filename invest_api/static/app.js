  function panelControlState(section, panel) {
    if (!section || !panel.controls) return [];
    const controls = section.querySelector('[data-role="panel-controls"]');
    if (!controls) return [];
    const entries = [];
    controls.querySelectorAll("input, select").forEach((input) => {
      if (input.type === "radio" && !input.checked) return;
      if (input.type === "checkbox" && !input.checked) return;
      entries.push([input.name, input.value]);
    });
    const portfolioValues = entries.filter(([key]) => key === "portfolios").map(([, value]) => value);
    if (portfolioValues.length) {
      for (let index = entries.length - 1; index >= 0; index -= 1) {
        if (entries[index][0] === "portfolios") entries.splice(index, 1);
      }
      entries.push(["portfolios", portfolioValues.join(",")]);
    }
    const start = entries.find(([key]) => key === "year_start");
    const end = entries.find(([key]) => key === "year_end");
    if (start && end && Number(start[1]) > Number(end[1])) {
      start[1] = end[1];
    }
    return entries;
  }

