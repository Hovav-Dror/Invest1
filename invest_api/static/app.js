  function xPositionValue(value, index) {
    if (typeof value === "number") return value;
    const date = Date.parse(value);
    if (Number.isFinite(date)) return date;
    const numeric = Number(value);
    return Number.isFinite(numeric) ? numeric : index;
  }

  function monthIndex(value) {
    const date = new Date(`${String(value).slice(0, 7)}-01T00:00:00Z`);
    return date.getUTCFullYear() * 12 + date.getUTCMonth();
  }

  function monthDate(index) {
    const year = Math.floor(index / 12);
    const month = (index % 12) + 1;
    return `${year}-${String(month).padStart(2, "0")}-01`;
  }

  function monthLabel(index) {
    const [year, month] = monthDate(index).split("-");
    return `${month}/${year}`;
  }

  function monthLabelIso(index) {
    const [year, month] = monthDate(index).split("-");
    return `${year}-${month}`;
  }

  function dateRangeTicks(minDate, maxDate) {
    const min = monthIndex(minDate);
    const max = monthIndex(maxDate);
    const span = max - min || 1;
    return Array.from({ length: 6 }, (_, index) => {
      const month = Math.round(min + (span * index) / 5);
      return {
        left: ((month - min) / span * 100).toFixed(2),
        label: monthLabelIso(month)
      };
    });
  }

  function updateDateRangeReadout() {
    const start = els.controls.querySelector('input[name="start_month"]');
    const end = els.controls.querySelector('input[name="end_month"]');
    const tracks = els.controls.querySelector(".date-range-tracks");
    if (!start || !end || !tracks) return;
    const startValue = Number(start.value);
    const endValue = Number(end.value);
    const min = Number(start.min);
    const max = Number(start.max);
    const low = Math.min(startValue, endValue);
    const high = Math.max(startValue, endValue);
    const lowPercent = max > min ? ((low - min) / (max - min)) * 100 : 0;
    const highPercent = max > min ? ((high - min) / (max - min)) * 100 : 100;
    const labels = tracks.querySelectorAll("[data-thumb-label]");
    if (labels[0]) {
      labels[0].textContent = monthLabelIso(low);
      labels[0].style.left = `${lowPercent}%`;
    }
    if (labels[1]) {
      labels[1].textContent = monthLabelIso(high);
      labels[1].style.left = `${highPercent}%`;
    }
    const years = els.controls.querySelector("[data-range-years]");
    const months = els.controls.querySelector("[data-range-months]");
    const monthCount = high - low + 1;
    if (years) years.textContent = Math.floor(monthCount / 12).toLocaleString(locale);
    if (months) months.textContent = monthCount.toLocaleString(locale);
    tracks.classList.toggle("labels-overlap", highPercent - lowPercent < 14);
    if (tracks && max > min) {
      tracks.style.setProperty("--range-start", `${lowPercent}%`);
      tracks.style.setProperty("--range-end", `${highPercent}%`);
    }
  }

