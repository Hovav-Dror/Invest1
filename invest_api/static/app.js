  function storyParagraphHtml(paragraph) {
    if (typeof paragraph === "string") return emphasisHtml(paragraph);
    if (Array.isArray(paragraph)) return paragraph.map(storyInlineHtml).join("");
    if (paragraph && paragraph.html) return paragraph.html;
    if (paragraph && paragraph.href && paragraph.linkText) {
      return `${escapeHtml(paragraph.textBefore || "")}<a href="${escapeHtml(paragraph.href)}"${storyLinkAttrs(paragraph.href)}>${escapeHtml(paragraph.linkText)}</a>${escapeHtml(paragraph.textAfter || "")}`;
    }
    return "";
  }

  function storyContentBlockHtml(block) {
    if (typeof block === "string" || Array.isArray(block) || (block && block.href && block.linkText)) {
      return `<p>${storyParagraphHtml(block)}</p>`;
    }
    if (!block) return "";
    if (block.html) return `<p>${block.html}</p>`;
    if (block.heading) return `<h3>${escapeHtml(block.heading)}</h3>`;
    if (block.subheading) return `<h4>${escapeHtml(block.subheading)}</h4>`;
    if (block.bold) return `<p class="story-emphasis">${escapeHtml(block.bold)}</p>`;
    if (block.text) return `<p>${emphasisHtml(block.text)}</p>`;
    return "";
  }

  function storyInlineHtml(part) {
    if (typeof part === "string") return escapeHtml(part);
    if (part && part.href && part.text) {
      return `<a href="${escapeHtml(part.href)}"${storyLinkAttrs(part.href)}>${escapeHtml(part.text)}</a>`;
    }
    return "";
  }

  function storyLinkAttrs(href) {
    return String(href).startsWith("#") ? "" : ' target="_blank" rel="noopener noreferrer"';
  }

