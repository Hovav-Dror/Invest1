  function lazyState() {
    const shared = els.chart.querySelector('[data-lazy-controls="a"]');
    const timeControls = els.chart.querySelector("[data-lazy-time-controls]");
    const sharedPrefix = shared.dataset.lazyControls;
    const checkedValues = (root, name) => [...root.querySelectorAll(`input[name="${name}"]:checked`)].map((input) => input.value);
    const valueOf = (root, name, fallback) => {
      const input = root.querySelector(`input[name="${name}"]:checked`) || root.querySelector(`input[name="${name}"]`);
      return input ? input.value : fallback;
    };
    return {
      value_mode: valueOf(shared, `value_mode_${sharedPrefix}`, "cpi_il"),
      portfolios: checkedValues(shared, "portfolios"),
      highlight: checkedValues(shared, "highlight"),
      factor_years: Number(valueOf(shared, "factor_years", "15")) || 15,
      year_start: Number(valueOf(shared, "year_start", "1955")) || 1955,
      year_end: Number(valueOf(shared, "year_end", "2023")) || 2023,
      commission_adjusted: Boolean(shared.querySelector('input[name="commission_adjusted"]:checked')),
      time_type: valueOf(timeControls, "time_type", "Accumulate"),
      rolling_window: Number(valueOf(timeControls, "rolling_window", "10")) || 10
    };
  }

  function syncLazyControls(source) {
    const sourceName = source.dataset.lazyControls;
    const target = els.chart.querySelector(`[data-lazy-controls="${sourceName === "a" ? "b" : "a"}"]`);
    if (!target) return;
    const targetName = target.dataset.lazyControls;
    const sourceMode = source.querySelector(`input[name="value_mode_${sourceName}"]:checked`);
    if (sourceMode) {
      target.querySelectorAll(`input[name="value_mode_${targetName}"]`).forEach((input) => {
        input.checked = input.value === sourceMode.value;
      });
    }
    ["factor_years", "year_start", "year_end"].forEach((name) => {
      const sourceInputs = [...source.querySelectorAll(`input[name="${name}"]`)];
      const targetInputs = [...target.querySelectorAll(`input[name="${name}"]`)];
      sourceInputs.forEach((input, index) => {
        if (!targetInputs[index]) return;
        if (input.type === "radio") targetInputs[index].checked = input.checked;
        else targetInputs[index].value = input.value;
      });
    });
    ["portfolios", "highlight"].forEach((name) => {
      const values = new Set([...source.querySelectorAll(`input[name="${name}"]:checked`)].map((input) => input.value));
      target.querySelectorAll(`input[name="${name}"]`).forEach((input) => {
        input.checked = values.has(input.value);
      });
    });
    const sourceCommission = source.querySelector('input[name="commission_adjusted"]');
    const targetCommission = target.querySelector('input[name="commission_adjusted"]');
    if (sourceCommission && targetCommission) targetCommission.checked = sourceCommission.checked;
  }

